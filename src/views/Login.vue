import RegForm from "../components/RegFrom";
import Captcha from "../components/Captcha";
import md5 from "js-md5";
export default {
  data() {
    return {
      loading: false,
      regVisible: false
    };
  },
  beforeCreate() {
    this.form = this.$form.createForm(this);
  },
  mounted() {
    document.querySelector(".bg").className += " animated bounceIn";
    document.querySelector("#weather-float-he").style.display = "block";
  },
  methods: {
    regCompleteHandler(data) {
      this.regVisible = false;
      this.form.setFieldsValue({
        username: data
      });
    },
    handleSubmit(e) {
      this.loading = true;
      e.preventDefault();
      this.form.validateFields((err, values) => {
        if (!err) {
          //提交
          // console.log("Received values of form: ", values);
          values.password = md5(values.password);
          this.$axios
            .post("api/login", values)
            .then(res => {
              switch (res.data.code) {
                case -1:
                  this.$error({
                    title: "错误",
                    content: res.data.msg
                  });
                  break;
                case 0:
                  // console.log(res.data.msg);
                  sessionStorage.setItem(
                    "userInfo",
                    JSON.stringify(res.data.msg)
                  );
                  this.$router.push("/index");
                  break;
              }
            })
            .catch(error => {
              console.log(error);
            });
        }
      });
      this.loading = false;
    }
  },
  beforeRouteLeave(to, from, next) {
    //隐藏天气
    document.querySelector("#weather-float-he").style.display = "none";
    next();
  },
  components: {
    RegForm,
    Captcha
  }
};
